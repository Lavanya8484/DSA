public class SimpleProgram {
    public static void addNumbers(int num1, int num2) {
        int sum = num1 + num2;
        System.out.println("Sum: " + sum);
    }
    public static void main(String[] args) {
        int number1 = 5;
        int number2 = 10;
        addNumbers(number1, number2);     }
}

